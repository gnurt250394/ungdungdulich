package com.example.trung.ngdngdulch.CustomGridview;

public class Obj_Grid {
    private int image;
    private String name;

    public Obj_Grid(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
